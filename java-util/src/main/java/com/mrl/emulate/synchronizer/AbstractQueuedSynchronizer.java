    /**
     * 纳秒级别的操作，主要是为了提供更高的响应速度
     */
    static final long spinForTimeoutThreshold = 1000L;

    /**
     * 从这里我们可以看出入队操作是从尾节点入队
     *
     * @param node
     * @return node 的前置节点
     */
    private Node enq(final Node node) {
        //这里采用死循环的方式设置，直到入队成功
        for (; ; ) {
            //从尾部入队
            Node t = tail;
            //如果没有尾节点， 说明当前队列尾空
            if (t == null) {
                //当队列中仅仅有一个节点的时候， 头结点==尾节点
                if (compareAndSetHead(new Node())) {
                    //头结点 == 尾节点
                    tail = head;
                }
            } else {
                //如果尾节点不为空，将当前节点的前置节点设置为尾节点。
                node.prev = t;
                //更新尾节点为当前节点
                if (compareAndSetTail(t, node)) {
                    t.next = node;
                    return t;
                }
            }
        }
    }


    /**
     * 设置等待节点
     *
     * @param mode Node.EXCLUSIVE for exclusive, Node.SHARED for shared
     * @return 返回新节点
     */
    private Node addWaiter(Node mode) {
        //设置等待节点
        Node node = new Node(Thread.currentThread(), mode);
        // 后面的代码都是入队操作
        // 快速定位的操作， 如果不行就调用 入队方法 ，这里减少了头定位问题的判断 ，不考虑效率 可以直接调用 入队方法enq ()
        Node pred = tail;
        if (pred != null) {
            node.prev = pred;
            if (compareAndSetTail(pred, node)) {
                pred.next = node;
                return node;
            }
        }
        enq(node);
        return node;
    }

    /**
     * 这里也是大师对高性能的要求 仅仅会使用在获取方法中。防止不必要的遍历，造成多余的内存消耗
     *
     * @param node the node
     */
    private void setHead(Node node) {
        head = node;
        node.thread = null;
        node.prev = null;
    }

    /**
     * 唤醒后续节点
     *
     * @param node
     */
    private void unparkSuccessor(Node node) {
        /**
         * fails or if status is changed by waiting thread.
         */
        int ws = node.waitStatus; //节点的状态
        if (ws < 0) {
            /*设置当前节点状态为0*/
            compareAndSetWaitStatus(node, ws, 0);
        }
        /**
         * 先判断NEXT 节点，如果next节点为空，则从尾部开始唤醒等待中的节点
         */
        Node s = node.next; // 获取后续节点 如果后续接待。或者后续节点的状态大于0
        if (s == null || s.waitStatus > 0) {
            //设置可用节点为空 这里方便GC操作 当下次JVM GC的时候此节点对象将必定会被回收
            s = null;
            // 从尾部循环当前队列，知道找到合适的为止
            for (Node t = tail; t != null && t != node; t = t.prev) {
                //如果节点的等待状态小于等于0 都说明节点在等待状态
                if (t.waitStatus <= 0) {
                    s = t;
                }
            }
        }
        /*释放后续满足条件节点的线程*/
        if (s != null) {
            LockSupport.unpark(s.thread);
        }
    }

    /**
     * 共享模式释放同步器  直到头结点成功整个说明同步器完成了释放
     */
    private void doReleaseShared() {
        for (; ; ) {
            //从头结点开始尝试释放操作
            Node h = head;
            if (h != null && h != tail) {
                int ws = h.waitStatus;
                //状态为-1
                if (ws == Node.SIGNAL) {
                    // 尝试释放同步器 如果失败 继续尝试
                    if (!compareAndSetWaitStatus(h, Node.SIGNAL, 0)) {
                        continue;            // loop to recheck cases
                    }
                    //调用释放后续节点状态
                    unparkSuccessor(h);
                } else if (ws == 0 && !compareAndSetWaitStatus(h, 0, Node.PROPAGATE)) {
                    continue;                // loop on failed CAS
                }
            }
            //直到头结点状态也改变
            if (h == head) {
                break; // loop if head changed
            }
        }
    }

    /**
     * 设置头节点
     *
     * @param node      某个节点
     * @param propagate 一个值
     *                  设置头结点，并释放当前同步器
     */
    private void setHeadAndPropagate(Node node, int propagate) {
        Node h = head;
        setHead(node);
        if (propagate > 0 || h == null || h.waitStatus < 0 || (h = head) == null || h.waitStatus < 0) {
            Node s = node.next;
            if (s == null || s.isShared()) {
                doReleaseShared();
            }
        }
    }

    /**
     * 将指定节点设置为取消状态
     *
     * @param node
     */
    private void cancelAcquire(Node node) {
        // 直接忽略节点不存在的情况 Ignore if node doesn't exist
        if (node == null) {
            return;
        }

        node.thread = null;
        // 跳过处理大于0 的前置节点 （1.表示已经处于取消状态）
        Node pred = node.prev;
        while (pred.waitStatus > 0) {
            node.prev = pred = pred.prev;
        }
        Node predNext = pred.next;
        // 设置当前节点取消状态
        node.waitStatus = Node.CANCELLED;
        // If we are the tail, remove ourselves.
        if (node == tail && compareAndSetTail(node, pred)) {
            compareAndSetNext(pred, predNext, null);
        } else {
            int ws;
            if (pred != head && ((ws = pred.waitStatus) == Node.SIGNAL || (ws <= 0 && compareAndSetWaitStatus(pred, ws, Node.SIGNAL))) && pred.thread != null) {
                Node next = node.next;
                if (next != null && next.waitStatus <= 0) {
                    compareAndSetNext(pred, predNext, next);
                }
            } else {
                unparkSuccessor(node);
            }
            node.next = node; // help GC
        }
    }

